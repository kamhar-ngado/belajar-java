public class increament
{
    public static void main(String[] args)
    {
        int a = 5;
        a++; //a++ or a=a+1
        int b= 4;
        b--; // b-- or b=b-1

        System.out.println("nilai a akan bertambah 1 sehingga a=" + a);
        System.out.println("nilai b akan berkurang 1 sehingga a=" + b);
    }
}